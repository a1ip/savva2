будет исключительно полезна для весьма широкого круга читателей. И особенно я бы рекомендовал
ее тем, кто еще не открыл для себя настоящую математическую красоту. Она не в формулах,
которые пугают, но в удивительно красивых, "строгих" рассуждениях. Только не ждите, что вам
расскажут о практических приложениях математических результатов. Математика прекрасна
изнутри, и именно в этом пафос книги».</div>

